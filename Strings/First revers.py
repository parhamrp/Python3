# defining a function to reverse an string
#  which is prompt by the user.
def Firstreverse (str_sample):
      final_str = str_sample [::-1] # [::-1] method used 
                                    # for reversing the function.
      return final_str
      # getting string from the user.
sample_str = input ('enter an string:')
print(Firstreverse(sample_str))
      
      
